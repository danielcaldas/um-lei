package exceptions;

/**Excepção para tratar casos em que um material não existe.
 * 
 * @author Jorge Caldas, José Cortez, Marcelo Gonçalves, Ricardo Silva
 * @version 2015.01.06
 */
public class TarefaNaoExisteException extends Exception {
    public TarefaNaoExisteException(int id){
        super("Tarefa com id: "+id+" não existe");
    }
    
    public TarefaNaoExisteException(String n){
        super("Tarefa com nome: "+n+" não existe");
    }
}
