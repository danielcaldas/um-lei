package exceptions;

/**Excepção para tratar casos em que um funcionário não existe.
 * 
 * @author Jorge Caldas, José Cortez, Marcelo Gonçalves, Ricardo Silva
 * @version 2015.01.06
 */

public class FuncionarioNaoExisteException extends Exception {
    public FuncionarioNaoExisteException(int id){
        super("Funcionario com id: "+id+" não existe");
    }
    
    public FuncionarioNaoExisteException(String user){
        super("Funcionario com username: "+user+" não existe");
    }
}
